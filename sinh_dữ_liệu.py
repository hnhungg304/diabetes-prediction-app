import pandas as pd
import numpy as np
import random

# Số lượng mẫu muốn tạo
num_samples = 2000

# Tạo một list để chứa các hàng dữ liệu
data_list = []

# Sinh dữ liệu giả
for _ in range(num_samples):
    # Age
    age = random.randint(20, 69)

    # Sex (Nam: 0, Nữ: 1)
    sex = random.choice([0, 1])

    # BMI
    bmi = round(np.random.normal(loc=22.5, scale=2.5), 1)  # Trung bình 22.5 ± 2.5

    # Waist Circumference
    if sex == 0:  # Nam
        waist_circumference = round(np.random.normal(loc=80, scale=5), 1)  # Trung bình 80 ± 5
    else:  # Nữ
        waist_circumference = round(np.random.normal(loc=70, scale=5), 1)  # Trung bình 70 ± 5

    # Fasting Blood Glucose
    fasting_blood_glucose = round(np.random.normal(loc=85, scale=5), 1)  # Trung bình 85 ± 5

    # HbA1c
    hba1c = round(np.random.normal(loc=5.2, scale=0.3), 1)  # Trung bình 5.2 ± 0.3

    # Family History of Diabetes
    family_history = random.choices([0, 1], weights=[0.7, 0.3])[0]  # 70% Không, 30% Có

    # Previous Gestational Diabetes (chỉ áp dụng cho nữ)
    previous_gestational_diabetes = 0
    if sex == 1:  # Nữ
        previous_gestational_diabetes = random.choices([0, 1], weights=[0.95, 0.05])[0]  # 95% Không, 5% Có

    # Output (Không mắc bệnh)
    output = 0

    # Thêm vào list dưới dạng dictionary
    data_list.append({
        'Age': age,
        'Sex': sex,
        'BMI': bmi,
        'Waist_Circumference': waist_circumference,
        'Fasting_Blood_Glucose': fasting_blood_glucose,
        'HbA1c': hba1c,
        'Family_History_of_Diabetes': family_history,
        'Previous_Gestational_Diabetes': previous_gestational_diabetes,
        'Output': output
    })

# Chuyển list thành DataFrame
data = pd.DataFrame(data_list)

# Hiển thị dữ liệu đầu tiên
print(data.head())

# Lưu file CSV
data.to_csv('/content/drive/MyDrive/non_diabetic_data.csv', index=False)
